import { Spark } from '../types/spark';

const now = '2026-06-14T10:00:00.000Z';

function demoMedia(sparkId: string, assetIds: string[]): Spark['media'] {
  return assetIds.map((assetId, index) => ({
    id: `${sparkId}-media-${index + 1}`,
    sparkId,
    mediaType: 'photo',
    url: `sparkbook-demo://${assetId}`,
    mutedByDefault: true,
    sortOrder: index,
    createdAt: now
  }));
}

function demoSpark(input: Omit<Spark, 'createdBy' | 'visibility' | 'audience' | 'status' | 'media' | 'createdAt' | 'updatedAt'> & {
  createdAt?: string;
  createdBy?: string;
  visibility?: Spark['visibility'];
  audience?: Spark['audience'];
  media?: Spark['media'];
}): Spark {
  const { createdAt, createdBy, visibility, audience, media, ...spark } = input;
  return {
    ...spark,
    createdBy: createdBy || 'profile-ray',
    visibility: visibility || 'public',
    audience: audience || visibility || 'public',
    status: 'active',
    media: media || [],
    createdAt: createdAt || now,
    updatedAt: now
  };
}

